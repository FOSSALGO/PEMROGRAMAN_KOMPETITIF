print("FOR TO--------------------------------")
for x in range(0,10+1,1):
  print(x,end=" ")

print("FOR DOWN TO---------------------------")
for x in range(13,0,-1):
  print(x,end=" ")

print("FOR EACH------------------------------")
A = [1,3,7,2,9,15,21,14]
for element in A:
  print(element,end=" ")

print("FOR TO in ARRAY-----------------------")
for i in range(0,len(A),1):
  print(A[i],end=" ")
